/*
 * A common set of methods that establish what a BarnyardAnimal must do.
 *
 * Classes that implement this interface must provide an implementation for
 * each method specified.
 * 
 * @author Kent Collins
 * @version 12 September, 2014
 */
 
public interface BarnyardAnimal {

	public void greetFarmer();  // what does your animal say to the farmer? print that to the console.
	
	public String toString();  // return a String description of yourself

}
