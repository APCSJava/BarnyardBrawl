/*
 * A common set of methods that all barnyard animals should
 * implement.  If a class wishes to serve as a barnyard animal
 * then it must (in some way) provide an implementation for
 * each method.
 * 
 * @author Kent Collins
 * @version 12 September, 2014
 */
public interface BarnyardAnimal {

	public void soundOff();  // print your sound to the console
	
	public String toString();  // return a String description of yourself

}
